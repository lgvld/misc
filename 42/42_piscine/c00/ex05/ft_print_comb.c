#include <unistd.h>

int		increasing_and_different(char d[])
{
	return (
		(d[0] < d[1] && d[1] < d[2]) &&
		(d[0] != d[1] && d[0] != d[2] && d[1] != d[2]));
}

void	ft_print_comb(void)
{
	char d[3];

	d[0] = '0';
	d[1] = '0';
	d[2] = '0';
	while (1)
	{
		if (increasing_and_different(d))
		{
			write(1, d, 3);
			if (d[0] == '7' && d[1] == '8' && d[2] == '9')
				break ;
			write(1, ", ", 2);
		}
		if (++d[2] > '9')
		{
			d[2] = '0';
			if (++d[1] > '9')
			{
				d[1] = '0';
				d[0]++;
			}
		}
	}
}
