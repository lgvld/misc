#include <unistd.h>

void	print(int n[])
{
	char c;

	c = '0' + n[0] / 10;
	write(1, &c, 1);
	c = '0' + n[0] % 10;
	write(1, &c, 1);
	write(1, " ", 1);
	c = '0' + n[1] / 10;
	write(1, &c, 1);
	c = '0' + n[1] % 10;
	write(1, &c, 1);
	if (!(n[0] == 98 && n[1] == 99))
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int n[2];

	n[0] = 0;
	n[1] = 0;
	while (n[0] <= 99)
	{
		if (n[0] < n[1])
		{
			print(n);
		}
		n[1]++;
		if (n[1] > 99)
		{
			n[1] = 0;
			n[0]++;
		}
	}
}
