int	ft_find_next_prime(int nb)
{
	int d;

	if (nb <= 2)
		return (2);
	if (nb % 2 == 0)
		nb++;
	d = 3;
	while (d * (d - 1) < nb)
	{
		if (nb % d++ == 0 && (d = 2))
			nb += 2;
		if (nb > 2147483629)
			return (2147483647);
	}
	return (nb);
}
